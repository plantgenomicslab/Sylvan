	parser.add_argument("--rex-qcovs", type=float, default=0.6, help="RexDB query coverage threshold (Default: 0.6)")
	parser.add_argument("--miniprot-cov", type=float, default=0.5, help="Miniprot coverage threshold for rescue (Default: 0.5)")
	parser.add_argument("--predictors", type=int, default=6, help="RF max_features per split (Default: 6)")
	parser.add_argument("--recycle", type=float, default=0.95, help="Confidence to recycle into training (Default: 0.95)")
	parser.add_argument("--trees", type=int, default=100, help="Number of RF trees (Default: 100)")
	parser.add_argument("--max-iter", type=int, default=5, help="Max RF re-training iterations (Default: 5)")
	parser.add_argument("--seed", type=int, default=123, help="Random seed (Default: 123)")
	parser.add_argument("--chromRegex", type=str, default="", help="Regex for chromosome name filtering")
	parser.add_argument("--output", type=str, default="filtered.gff3", help="Output GFF3 path")
	parser.add_argument("--output-dir", type=str, default="FILTER", help="Output directory for data files")
	args = parser.parse_args()

	output_dir = args.output_dir
	output_file = args.output

	# Collect evidence and assign initial labels
	data = filter_genes(
		args.gff, args.tpm, args.rcov, args.acov, args.hcov, args.repcov,
		args.blast_pident, args.blast_qcovs, args.rex_pident, args.rex_qcovs,
		args.miniprot_cov, output_dir,
	)
	data.to_csv(f"{output_dir}/data.tsv", sep="\t", index=False)

	# Run semi-supervised RF
	keep, report = semiSupRandomForest(
		data, args.predictors, args.busco, args.trees,
		seed=args.seed, recycle_prob=args.recycle, maxiter=args.max_iter,
	)
	pprint(report)

	# Reformat GFF with standardized IDs via TidyGFF
	TidyGFF.tidyGFF(output_dir, args.gff, True, "tidy.gff", "t", 8, True, True,
	                chrom_regex=args.chromRegex)
	map_file = pd.read_csv(
		f"{os.path.basename(args.gff)}.map", sep="\t",
		names=["transcript_id", "New_ID"], usecols=[1, 2],
	)
	gff_data = read_gff_file("tidy.gff")

	keep = keep.merge(map_file, on="transcript_id", how="left")
	keep["basenames"] = keep['New_ID'].apply(_format_gene_id)

	# Split GFF into kept and discarded
	gff_keep, gff_discard = filter_gff(gff_data, keep)

	# Classify discarded genes by reason
	keep_ids = set(keep["transcript_id"])
	data_indexed = data.set_index("transcript_id")
	keep_data = data_indexed[data_indexed.index.isin(keep_ids)].copy()
	discard_data = data_indexed[~data_indexed.index.isin(keep_ids)].copy()
	discard_data["discard_reason"] = _classify_discard(discard_data)

	# Build mapping: New_ID (mRNA) → discard_reason
	# map_file maps original transcript_id → New_ID; discard_data has original transcript_id
	discard_map = map_file[map_file["transcript_id"].isin(discard_data.index)].copy()
	discard_map = discard_map.merge(
		discard_data[["discard_reason"]].reset_index(),
		on="transcript_id", how="left",
	)
	# Map both mRNA ID and gene ID (mRNA minus .tN suffix)
	reason_lookup = {}
	for _, row in discard_map.iterrows():
		new_id = row["New_ID"]
		reason = row["discard_reason"]
		reason_lookup[new_id] = reason
		gene_id = new_id.rsplit(".", 1)[0] if ".t" in new_id else new_id
		reason_lookup[gene_id] = reason

	# Annotate discard GFF3 column 9 with discard_reason attribute
	def _add_reason_attr(row):
		gff_id = _get_gff_id(row[8])
		reason = reason_lookup.get(gff_id)
		if reason is None:
			parent = _get_gff_parent(row[8])
			reason = reason_lookup.get(parent, "pseudogene")
		attr = row[8].rstrip(";")
		return f"{attr};discard_reason={reason}"

	gff_discard = gff_discard.copy()
	gff_discard[8] = gff_discard.apply(_add_reason_attr, axis=1)

	# Write output GFF3 files
	gff_keep.iloc[:, 0:9].to_csv(output_file, sep="\t", index=False, header=False)
	gff_discard.iloc[:, 0:9].to_csv(f"{output_dir}/discard.gff3", sep="\t", index=False, header=False)
	keep_data.to_csv(f"{output_dir}/keep_data.tsv", sep="\t")
	discard_data.to_csv(f"{output_dir}/discard_data.tsv", sep="\t")

	# Display summary
	print(f"\nTotal genes\t: {sum(gff_data[2] == 'gene')}")
	print(f"Total mRNA\t: {sum(gff_data[2] == 'mRNA')}\n")
	print(f"\nGenes kept\t : {sum(gff_keep[2] == 'gene')}")
	print(f"mRNA kept\t : {sum(gff_keep[2] == 'mRNA')}\n")
	print(f"\nGenes discarded: {sum(gff_discard[2] == 'gene')}")
	print(f"mRNA discarded: {sum(gff_discard[2] == 'mRNA')}\n")

	# Discard reason breakdown
	reason_counts = discard_data["discard_reason"].value_counts()
	print("Discard reason breakdown:")
	for reason, count in reason_counts.items():
		print(f"  {reason}: {count}")
	print()

	print(f"Filtered gene models written to: {output_file}")
	print(f"Discarded gene models written to: {output_dir}/discard.gff3\n")
	print(f"Filter data written to: {output_dir}/discard_data.tsv   {output_dir}/keep_data.tsv")
	print(f"See {args.gff}.map for associations with original IDs.")
