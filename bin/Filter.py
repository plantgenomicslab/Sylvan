"""Semi-supervised random forest filter for genome annotation gene models.

Integrates multiple evidence sources (expression, homology, protein domains,
ab initio predictions, repeat overlap, lncRNA classification) to label gene
models as Keep/Discard, then iteratively trains a random forest to classify
the remaining undecided models.

Evidence files (generated by Snakefile_filter):
    FILTER/BLASTP.OUT.TMP         — BLASTp vs protein database
    FILTER/BLASTP.OUT.Rex         — BLASTp vs RexDB (transposon proteins)
    FILTER/cov.bed                — RNA-seq coverage per transcript
    FILTER/pfam.out               — PfamScan domain hits
    FILTER/rsem_outdir/RSEM.isoforms.results — RSEM expression (TPM)
    FILTER/augustus_coverage.bed   — Augustus prediction overlap
    FILTER/helixer_coverage.bed   — Helixer prediction overlap
    FILTER/repeat_coverage.bed    — Repeat element overlap
    FILTER/lncrna_predict.csv     — lncDC lncRNA predictions
"""

import os
import re
import sys
import argparse
from pprint import pprint
import pandas as pd
import numpy as np
from sklearn.ensemble import RandomForestClassifier
import TidyGFF


# ---------------------------------------------------------------------------
# Evidence file readers
# ---------------------------------------------------------------------------

def read_rsem_file(file_path):
	"""Read RSEM isoform-level expression results."""
	return pd.read_csv(file_path, delimiter="\t")


def read_blast_file(file_path):
	"""Read BLAST tabular output (outfmt 6): transcript_id, pident, qcovs."""
	return pd.read_csv(
		file_path, header=None, sep="\t",
		usecols=[0, 2, 12],
		names=["transcript_id", "pident", "qcovs"],
	)


def read_cov_file(file_path):
	"""Read bedtools coverage output: transcript_id, coverage fraction."""
	return pd.read_csv(file_path, header=None, sep="\t", usecols=[0, 6])


def read_pfam_file(file_path):
	"""Read PfamScan output (whitespace-delimited, # comments)."""
	return pd.read_csv(file_path, header=None, sep=r'\s+', comment='#', usecols=[0])


def read_gff_file(file_path):
	"""Read a 9-column GFF3 file into a DataFrame."""
	return pd.read_csv(file_path, header=None, sep="\t")


def read_abInitio_cov(file_path):
	"""Read ab initio / repeat coverage BED: transcript_id (col 4), coverage (col 8)."""
	return pd.read_csv(file_path, header=None, sep="\t", usecols=[4, 8])


# ---------------------------------------------------------------------------
# Evidence integration and initial labeling
# ---------------------------------------------------------------------------

def filter_genes(gff_path, tpm_cutoff, cov_cutoff, augustus_cutoff, helixer_cutoff,
                 repeat_cutoff, blast_pident, blast_qcovs, rex_pident, rex_qcovs,
                 miniprot_cutoff=0.5, output_dir="FILTER"):
	"""Collect all evidence sources, apply cutoffs, and assign initial Keep/Discard/None labels.

	Two parallel DataFrames are built:
	  - ``data``:      raw numeric values for RF feature input
	  - ``filter_df``: boolean flags (above/below cutoff) for heuristic labeling

	Args:
		gff_path:        Path to input GFF3 (for single-exon gene detection)
		tpm_cutoff:      RSEM TPM threshold
		cov_cutoff:      RNA-seq coverage fraction threshold
		augustus_cutoff:  Augustus prediction overlap threshold
		helixer_cutoff:  Helixer prediction overlap threshold
		repeat_cutoff:   Repeat overlap threshold (genes BELOW this are kept)
		blast_pident:    BLASTp percent identity threshold (0–1 scale, multiplied by 100)
		blast_qcovs:     BLASTp query coverage threshold (0–1 scale)
		rex_pident:      RexDB percent identity threshold (0–1 scale)
		rex_qcovs:       RexDB query coverage threshold (0–1 scale)
		miniprot_cutoff: Miniprot protein alignment overlap threshold for rescue
		output_dir:      Directory containing evidence files

	Returns:
		DataFrame with columns: transcript_id, evidence values, and label (Keep/Discard/None)
	"""
	# --- RSEM expression ---
	rsem_file = os.path.join(output_dir, "rsem_outdir", "RSEM.isoforms.results")
	rsem_data = read_rsem_file(rsem_file)
	filter_df = rsem_data.loc[:, ("transcript_id", "TPM")].copy()
	data = rsem_data.loc[:, ("transcript_id", "TPM")].copy()
	filter_df["TPM"] = rsem_data["TPM"] > float(tpm_cutoff)

	# --- BLASTp homology ---
	blast_file = os.path.join(output_dir, "BLASTP.OUT.TMP")
	blast_data = read_blast_file(blast_file)
	blast_data.drop_duplicates(subset=["transcript_id"], keep='first', inplace=True, ignore_index=True)
	blast_data["blast_pident"] = blast_data["pident"]
	blast_data["blast_qcovs"] = blast_data["qcovs"]
	data = data.merge(blast_data.loc[:, ("transcript_id", "blast_pident", "blast_qcovs")], on="transcript_id", how="left")
	blast_data["blast_pident"] = blast_data["pident"] > float(blast_pident) * 100
	blast_data["blast_qcovs"] = blast_data["qcovs"] > float(blast_qcovs) * 100
	filter_df = filter_df.merge(blast_data.loc[:, ("transcript_id", "blast_pident", "blast_qcovs")], on="transcript_id", how="left")

	# --- RNA-seq coverage ---
	cov_file = os.path.join(output_dir, "cov.bed")
	cov_data = read_cov_file(cov_file)
	cov_data.columns = ["transcript_id", "COVERAGE"]
	data = data.merge(cov_data, on="transcript_id", how="left")
	cov_data["COVERAGE"] = cov_data["COVERAGE"] > float(cov_cutoff)
	filter_df = filter_df.merge(cov_data, on="transcript_id", how="left")

	# --- Pfam protein domains ---
	pfam_file = os.path.join(output_dir, "pfam.out")
	pfam_data = read_pfam_file(pfam_file)
	pfam_data = pfam_data[0].unique()
	pfam_data = pd.DataFrame(pfam_data, columns=["transcript_id"])
	pfam_data["PFAM"] = True
	data = data.merge(pfam_data, on="transcript_id", how="left")
	filter_df = filter_df.merge(pfam_data, on="transcript_id", how="left")
	data.drop_duplicates(subset=["transcript_id"], keep='first', inplace=True, ignore_index=True)
	filter_df.drop_duplicates(subset=["transcript_id"], keep='first', inplace=True, ignore_index=True)

	# --- Ab initio prediction overlap ---
	augustus_cov = read_abInitio_cov(os.path.join(output_dir, "augustus_coverage.bed"))
	helixer_cov = read_abInitio_cov(os.path.join(output_dir, "helixer_coverage.bed"))
	augustus_cov.columns = ["transcript_id", "AUGUSTUS"]
	helixer_cov.columns = ["transcript_id", "HELIXER"]

	data = data.merge(augustus_cov, on="transcript_id", how="left")
	data = data.merge(helixer_cov, on="transcript_id", how="left")

	augustus_cov["AUGUSTUS"] = augustus_cov["AUGUSTUS"] > float(augustus_cutoff)
	helixer_cov["HELIXER"] = helixer_cov["HELIXER"] > float(helixer_cutoff)
	filter_df = filter_df.merge(augustus_cov, on="transcript_id", how="left")
	filter_df = filter_df.merge(helixer_cov, on="transcript_id", how="left")

	# --- Miniprot protein alignment overlap ---
	miniprot_cov_file = os.path.join(output_dir, "miniprot_coverage.bed")
	if os.path.exists(miniprot_cov_file):
		miniprot_cov = read_abInitio_cov(miniprot_cov_file)
		miniprot_cov.columns = ["transcript_id", "MINIPROT"]
		data = data.merge(miniprot_cov, on="transcript_id", how="left")
		miniprot_cov["MINIPROT"] = miniprot_cov["MINIPROT"] > float(miniprot_cutoff)
		filter_df = filter_df.merge(miniprot_cov, on="transcript_id", how="left")

	# --- Repeat element overlap ---
	# REPEAT=True means coverage < cutoff (NOT a repeat → good gene)
	# REPEAT=False means coverage ≥ cutoff (IS a repeat → likely TE)
	repeat_cov = read_abInitio_cov(os.path.join(output_dir, "repeat_coverage.bed"))
	repeat_cov.columns = ["transcript_id", "REPEAT"]
	data = data.merge(repeat_cov, on="transcript_id", how="left")
	repeat_cov["REPEAT"] = repeat_cov["REPEAT"] < float(repeat_cutoff)
	filter_df = filter_df.merge(repeat_cov, on="transcript_id", how="left")

	# --- Single-exon genes ---
	singleExons = TidyGFF.singleExonGenes(TidyGFF.loadGFF(gff_path))
	singleExons["singleExon"] = True
	data = data.merge(singleExons, on="transcript_id", how="left")
	filter_df = filter_df.merge(singleExons, on="transcript_id", how="left")

	# --- RexDB transposon protein hits ---
	rexdb = os.path.join(output_dir, "BLASTP.OUT.Rex")
	rexdb = read_blast_file(rexdb)
	rexdb.drop_duplicates(subset=["transcript_id"], keep='first', inplace=True, ignore_index=True)
	rexdb["rex_pident"] = rexdb["pident"]
	rexdb["rex_qcovs"] = rexdb["qcovs"]
	data = data.merge(rexdb.loc[:, ("transcript_id", "rex_pident", "rex_qcovs")], on="transcript_id", how="left")
	rexdb["rex_pident"] = rexdb["pident"] > float(rex_pident) * 100
	rexdb["rex_qcovs"] = rexdb["qcovs"] > float(rex_qcovs) * 100
	filter_df = filter_df.merge(rexdb.loc[:, ("transcript_id", "rex_pident", "rex_qcovs")], on="transcript_id", how="left")

	# --- lncRNA prediction ---
	lncrna = pd.read_csv(os.path.join(output_dir, "lncrna_predict.csv"), comment='#', usecols=[0, 25], names=["transcript_id", "LncRNA_predict"])
	lncrna["transcript_id"] = lncrna["transcript_id"].str.split(" ").str.get(0)
	lncrna["LncRNA_predict"] = lncrna["LncRNA_predict"] == "lncrna"
	data = data.merge(lncrna, on="transcript_id", how="left")
	filter_df = filter_df.merge(lncrna, on="transcript_id", how="left")

	# --- Fill NaN in boolean columns (pandas 3.0: NaN comparisons return False) ---
	bool_cols = ["TPM", "COVERAGE", "blast_pident", "blast_qcovs", "PFAM", "AUGUSTUS",
	             "HELIXER", "MINIPROT", "REPEAT", "singleExon", "rex_pident", "rex_qcovs", "LncRNA_predict"]
	for col in bool_cols:
		if col in filter_df.columns:
			filter_df[col] = filter_df[col].fillna(False)

	# --- Named boolean masks for readability ---
	not_repeat = (filter_df["REPEAT"] == True)
	is_repeat  = (filter_df["REPEAT"] == False)
	has_blast   = (filter_df["blast_pident"] == True) & (filter_df["blast_qcovs"] == True)
	no_blast    = (filter_df["blast_pident"] == False) & (filter_df["blast_qcovs"] == False)
	has_rex     = (filter_df["rex_pident"] == True) & (filter_df["rex_qcovs"] == True)
	has_pfam    = (filter_df["PFAM"] == True)
	has_aug     = (filter_df["AUGUSTUS"] == True)
	has_hel     = (filter_df["HELIXER"] == True)
	has_cov     = (filter_df["COVERAGE"] == True)
	has_tpm     = (filter_df["TPM"] == True)
	multi_exon  = (filter_df["singleExon"] == False)
	is_lncrna   = (filter_df["LncRNA_predict"] == True)

	# === KEEP: seed labels for RF training ===
	keep_cond = (
		# Path 1: RNA-seq coverage + functional/structural evidence
		(has_cov & ((has_blast & has_pfam) | (has_aug & has_hel))) |
		# Path 2: expressed multi-exon gene (not lncRNA)
		(multi_exon & ~is_lncrna & has_cov & has_tpm) |
		# Path 3: multi-exon with protein domain + homology (no expression needed)
		(multi_exon & has_pfam & has_blast) |
		# Path 4: both ab initio predictors agree, not TE, multi-exon
		(has_aug & has_hel & ~has_rex & not_repeat & ~is_lncrna & multi_exon) |
		# Path 5: homology + protein domain (strong functional evidence)
		(has_blast & has_pfam)
	)
	data["keep"] = keep_cond.values

	# === DISCARD: seed labels for RF training (pseudogene/TE removal) ===
	discard_cond = (
		# Path 1: no evidence at all
		(~has_cov & ~has_aug & ~has_hel & no_blast & ~has_pfam) |
		# Path 2: single-exon with no homology, no domain, no expression
		(~multi_exon & no_blast & ~has_pfam & ~has_cov) |
		# Path 3: TE-like (RexDB hit) with no real homology or domain
		(has_rex & no_blast & ~has_pfam) |
		# Path 4: lncRNA + single-exon + no protein evidence
		(is_lncrna & ~multi_exon & ~has_pfam & ~has_aug & ~has_hel) |
		# Path 5: Augustus-only — no blast, no pfam, no Helixer, no expression
		(has_aug & ~has_hel & no_blast & ~has_pfam & ~has_cov)
	)
	data["discard"] = discard_cond.values

	# Assign label: Discard takes priority over Keep, remainder is None (undecided)
	data["label"] = np.select(
		[data["discard"], data["keep"]],
		["Discard", "Keep"],
		default="None",
	)
	return data.drop(columns=["discard", "keep"])


