def filter_gff(gff_data, keep):
	"""Split GFF3 into kept and discarded rows based on transcript IDs."""
	gff_data["transcript_id"] = gff_data[8].apply(_get_gff_id)
	gff_data["parent_id"] = gff_data[8].apply(_get_gff_parent)
	names = keep['New_ID']
	# Include gene-level IDs (strip .tN suffix only) to match gene features
	names = pd.concat([names, names.str.replace(r'\.t\d+$', '', regex=True)])
	to_keep = gff_data["transcript_id"].isin(names) | gff_data["parent_id"].isin(names)
	return gff_data[to_keep], gff_data[~to_keep]


def _get_gff_id(attr):
	"""Extract ID= value from a GFF3 attribute string."""
	m = re.search(r"ID=([^;\s]+)", attr)
	return m.group(1) if m else None


def _get_gff_parent(attr):
	"""Extract Parent= value from a GFF3 attribute string."""
	m = re.search(r"Parent=([^;\s]+)", attr)
	return m.group(1) if m else None


def _format_gene_id(transcript_id):
	"""Strip transcript suffix (.t1, .t2, ...) to get the base gene ID."""
	if pd.isna(transcript_id):
		return transcript_id
	match = re.search(r"^(.+?)(?:\.t\d+)?$", str(transcript_id))
	return match.group(1) if match else transcript_id


# ---------------------------------------------------------------------------
# RF feature preparation
# ---------------------------------------------------------------------------

def prepare_features(df, feature_cols):
	"""Build RF feature matrix with explicit missingness indicators.

	For each evidence column, adds a binary ``<col>_missing`` flag so the RF
	can distinguish "no evidence" from "evidence = 0".  Remaining NaN values
	are imputed to 0.
	"""
	features = df.loc[:, feature_cols].copy()
	for col in feature_cols:
		features[f"{col}_missing"] = features[col].isna().astype(int)
		features[col] = pd.to_numeric(features[col], errors="coerce").fillna(0)
	return features


# ---------------------------------------------------------------------------
# Semi-supervised random forest
# ---------------------------------------------------------------------------

RF_RESCUE_THRESHOLD = 0.6  # Minimum RF Keep probability to rescue undecided genes

def semiSupRandomForest(data, predictors, busco_table, num_trees,
                        seed=None, recycle_prob=0.95, maxiter=5):
	"""Iterative semi-supervised random forest for gene model classification.

	Starting from heuristic Keep/Discard seed labels, trains an RF and
	recycles high-confidence predictions (>= recycle_prob) into the training
	set.  Repeats until no new genes are recycled or maxiter is reached.

	Remaining undecided genes are rescued via two tiers:
	  1. RF Keep probability > RF_RESCUE_THRESHOLD (excluding TE-only genes)
	  2. Pfam domain present, not repeat-dominated, no RexDB hit

	Args:
		data:         DataFrame from filter_genes() with 'label' column
		predictors:   Number of features considered per RF split (max_features)
		busco_table:  Path to BUSCO full_table.tsv for monitoring
		num_trees:    Number of trees in the RF ensemble
		seed:         Random seed for reproducibility
		recycle_prob: Confidence threshold to recycle predictions into training
		maxiter:      Maximum number of re-training iterations

	Returns:
		(kept_df, process_dict): kept transcript IDs and training diagnostics
	"""
	# Load BUSCO table to monitor completeness during training
	busco = pd.read_csv(busco_table, sep="\t", comment="#", usecols=(0, 1, 2),
	                     names=["busco_id", "status", "transcript_id"])
	busco = busco.loc[busco["status"] != "Missing"].reset_index(drop=True)

	# Split into labeled (train) and unlabeled (test)
	train = data.loc[data["label"] != "None"].set_index("transcript_id")
	test = data.loc[data["label"] == "None"].set_index("transcript_id")
	feature_cols = [c for c in data.columns if c not in ("transcript_id", "label")]

	x_train = prepare_features(train, feature_cols)
	y_train = train.loc[:, train.columns == "label"]
	x_test = prepare_features(test, feature_cols)

	process = {"kept": [], "discarded": [], "kept_buscos": [], "discarded_buscos": [], "OOB": []}

	for iteration in range(maxiter):
		discarded = y_train[y_train["label"] == "Discard"].index
		kept = y_train[y_train["label"] == "Keep"].index

		process["discarded"].append(len(discarded))
		process["kept"].append(len(kept))

		# Count BUSCOs in kept/discarded sets
		kept_buscos = busco.loc[busco["transcript_id"].isin(kept), "busco_id"].unique()
		dis_buscos = busco.loc[busco["transcript_id"].isin(discarded), "busco_id"].unique()
		process["discarded_buscos"].append(len(dis_buscos))
		process["kept_buscos"].append(len(kept_buscos))

		# Train RF
		model = RandomForestClassifier(
			n_estimators=num_trees,
			max_features=predictors,
			random_state=seed,
			oob_score=True,
		)
		model.fit(x_train, y_train.to_numpy().reshape(-1,))
		process["OOB"].append(1 - model.oob_score_)

		# Predict unlabeled genes
		yhat = pd.DataFrame({
			"prediction": model.predict(x_test),
			"transcript_id": x_test.index,
		})
		yprob = pd.DataFrame(model.predict_proba(x_test), columns=model.classes_)
		yhat = pd.concat([yhat.reset_index(drop=True), yprob], axis=1)
		yhat["pred_prob"] = yhat.apply(lambda row: row[row["prediction"]], axis=1)

		# Recycle high-confidence predictions into training set
		confident = yhat[yhat["pred_prob"] > recycle_prob]
		if len(confident) == 0:
			break

		# Merge confident predictions back with full feature data
		recycle = confident[["transcript_id", "prediction"]].copy()
		recycle = recycle.merge(data, on="transcript_id", how="left")
		recycle["label"] = recycle["prediction"]
		recycle = recycle.set_index("transcript_id")

		recycle_features = prepare_features(recycle, feature_cols)
		x_train = pd.concat([x_train, recycle_features], axis=0)
		y_train = pd.concat([y_train, recycle.loc[:, recycle.columns == "label"]], axis=0)
		x_test = x_test[~x_test.index.isin(recycle.index)]

	else:
		# for-else: only runs if the loop was NOT broken out of (i.e., hit maxiter)
		print(f"Training stopped at a maximum of {maxiter} iterations.")

	# --- Rescue undecided genes ---
	if len(x_test) > 0:
		final_prob = pd.DataFrame(
			model.predict_proba(x_test), columns=model.classes_, index=x_test.index,
		)
		keep_prob = final_prob["Keep"] if "Keep" in final_prob.columns else pd.Series(0, index=x_test.index)

		# Tier 1: RF predicts Keep above rescue threshold
		rf_keep = keep_prob[keep_prob > RF_RESCUE_THRESHOLD].index.tolist()

		# Identify TE-only genes: RexDB hit without BLAST homology = transposon
		evidence = data.set_index("transcript_id").reindex(x_test.index)
		te_genes = set()
		if "rex_pident" in evidence.columns and "blast_pident" in evidence.columns:
			has_rex = evidence["rex_pident"].fillna(0) > 0
			no_blast = ~(evidence["blast_pident"].fillna(0) > 0)
			te_genes = set(evidence[has_rex & no_blast].index)
		rf_keep = [g for g in rf_keep if g not in te_genes]

		# Tier 2: Pfam domain present — keep unless repeat-dominated or TE-derived
		pfam_rescue = []
		if "PFAM" in evidence.columns and "REPEAT" in evidence.columns:
			has_pfam = evidence["PFAM"] == True
			not_repeat = evidence["REPEAT"].fillna(0) <= 0.5
			no_rexdb = ~(evidence["rex_pident"].fillna(0) > 0) if "rex_pident" in evidence.columns else True
			pfam_rescue = evidence[has_pfam & not_repeat & no_rexdb].index.tolist()

		# Tier 3: BUSCO safety net — never discard a gene with a Complete BUSCO hit
		busco_complete = busco.loc[busco["status"].isin(["Complete", "Duplicated"]), "transcript_id"]
		busco_rescue = [g for g in x_test.index if g in busco_complete.values]

		# Combine all tiers (deduplicated)
		# Note: MINIPROT column is available as an RF feature but not used as
		# a rescue condition — miniprot coverage alone does not discriminate
		# between functional genes and pseudogenes in undecided sets.
		rescued = list(set(rf_keep + pfam_rescue + busco_rescue))
		undecided_disc = len(x_test) - len(rescued)
		kept = kept.tolist() + rescued
		print(f"Undecided None genes: {len(x_test)} "
		      f"(RF>{RF_RESCUE_THRESHOLD}: {len(rf_keep)}, "
		      f"Pfam rescue: {len(pfam_rescue)}, "
		      f"BUSCO rescue: {len(busco_rescue)}, "
		      f"TE excluded: {len(te_genes)}, "
		      f"total kept: {len(rescued)}, discarded: {undecided_disc})")

	return pd.DataFrame(kept, columns=["transcript_id"]), process


def _classify_discard(df):
	"""Classify discarded genes into three categories based on evidence.

	Categories (applied in priority order):
	  TE_related:  RexDB hit (transposon-derived or TE-invaded gene)
	  lncRNA:      Predicted as lncRNA by lncDC classifier
	  pseudogene:  Everything else (low/no evidence, non-functional)

	Args:
		df: DataFrame indexed by transcript_id with evidence columns

	Returns:
		Series of discard reason strings
	"""
	reasons = pd.Series("pseudogene", index=df.index)

	has_rex = df["rex_pident"].fillna(0) > 0 if "rex_pident" in df.columns else pd.Series(False, index=df.index)
	is_lncrna = df["LncRNA_predict"].fillna(0) > 0.5 if "LncRNA_predict" in df.columns else pd.Series(False, index=df.index)

	# Priority: TE_related > lncRNA > pseudogene
	reasons[has_rex] = "TE_related"
	reasons[~has_rex & is_lncrna] = "lncRNA"

	return reasons


