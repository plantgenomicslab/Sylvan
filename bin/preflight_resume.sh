#!/usr/bin/env bash
# preflight_resume.sh <run-dir> [--apply]
#
# Prepare an interrupted `annotate` run for a resume on the v2.1.0 container.
#
# WHY THIS EXISTS
#   A run that stopped before v2.1.0 carries three kinds of debris, and only one
#   of them is visible to Snakemake:
#
#     * `gmapExon.gff3` (0 bytes) and `geneRegion_genewise.tmp/` (partial) belong
#       to jobs that FAILED. Snakemake records them as incomplete and
#       `--rerun-incomplete` redoes them. Nothing to do.
#
#     * `liftoffExon.fasta` (0 bytes) belongs to a job that SUCCEEDED. `gmapPrep`
#       ends in `bedtools getfasta ... || touch liftoffExon.fasta`, so when
#       `bedtools` was missing from $PATH (fixed in d190fcc) the rule still exited
#       0. Snakemake stored it as complete, and its output is newer than its
#       input, so NEITHER `--rerun-incomplete` NOR `--rerun-triggers mtime` will
#       redo it. Left alone, `gmapExon` re-runs against an empty FASTA and the
#       whole GMAP exon evidence track silently disappears from the EVM consensus.
#       The only way to force it is to delete the output.
#
#     * `.micromamba_hook.sh` was generated by the OLD image. The Snakefile only
#       rebuilds it when absent ("Delete the cache when the .sif image changes").
#
# WHY NOT JUST `./bin/annotate.sh`
#   Snakemake 7's default `--rerun-triggers` includes `code`, and v2.1.0 changed
#   the body of `helixer`, `pickBetterModels`, `STAR_single`, `GETA_output`, ...
#   A bare resume therefore re-runs Helixer on the GPU, and everything below it,
#   for no benefit. Resume with `--rerun-triggers mtime` and delete the three
#   artifacts above by hand -- which is what `--apply` does.
#
# Dry-run by default. Pass --apply to make changes. Nothing is deleted: every
# artifact is moved into results/_preflight_backup_<stamp>/.
set -euo pipefail

PUBLISHED_SIF="${SYLVAN_SIF:-/data/gpfs/assoc/pgl/singularity/Sylvan/Sylvan_20260708b.sif}"

RUN="${1:?usage: preflight_resume.sh <run-dir> [--apply]}"
shift
APPLY=no
CONFIG="${SYLVAN_CONFIG:-}"
while [ $# -gt 0 ]; do
	case "$1" in
		--apply)  APPLY=yes ;;
		--config) CONFIG="${2:?--config needs a path}"; shift ;;
		*) printf 'unknown argument: %s\n' "$1" >&2; exit 2 ;;
	esac
	shift
done

[ -d "$RUN" ] || { printf 'not a directory: %s\n' "$RUN" >&2; exit 1; }
RUN="$(cd "$RUN" && pwd)"

say()  { printf '%s\n' "$*"; }
die()  { printf 'ERROR: %s\n' "$*" >&2; exit 1; }
head2() { printf '\n== %s\n' "$*"; }

# ---- 1. refuse to prepare a run whose bin/ predates v2.1.0 ------------------
head2 "code"
SNAKEFILE="$RUN/bin/Snakefile_annotate"
[ -f "$SNAKEFILE" ] || die "missing $SNAKEFILE"

grep -q 'Rank='                         "$RUN/bin/miniprot2Genewise.py" || die "bin/miniprot2Genewise.py predates the best-hit fix (5bc3325)"
if grep -q 'def readFasta(path: str, sep=" "' "$RUN/bin/fasta_utils.py"; then
	die "bin/fasta_utils.py predates the whitespace-ID fix (ce3f951)"
fi
grep -q 'GMAP_BIN'                      "$SNAKEFILE" || die "bin/Snakefile_annotate predates the gmap/gmapl size fix (ce3f951)"
grep -q '/opt/envs/alignment/bin/bedtools' "$SNAKEFILE" || die "bin/Snakefile_annotate predates the gmapPrep bedtools pin (d190fcc)"
say "bin/ carries the v2.1.0 fixes (best-hit, whitespace IDs, gmap size select, bedtools pin)"

# ---- 2. container -----------------------------------------------------------
head2 "container"
CONFIG="${CONFIG:-config/config_annotate.yml}"
case "$CONFIG" in /*) ;; *) CONFIG="$RUN/$CONFIG" ;; esac
[ -f "$CONFIG" ] || die "config not found: $CONFIG  (pass --config, or set SYLVAN_CONFIG)"

CFG_SIF=$(awk '/^[[:space:]]*singularity:[[:space:]]*[^[:space:]#]/ {
	sub(/^[^:]*:[[:space:]]*/, ""); sub(/[[:space:]]*#.*$/, ""); print; exit }' "$CONFIG")
[ -n "$CFG_SIF" ] || die "no 'singularity:' key in $CONFIG"
case "$CFG_SIF" in /*) ;; *) CFG_SIF="$RUN/$CFG_SIF" ;; esac

[ -f "$PUBLISHED_SIF" ] || die "published image not readable: $PUBLISHED_SIF"

SIF_OK=no
if [ -e "$CFG_SIF" ] && [ "$(readlink -f "$CFG_SIF")" = "$(readlink -f "$PUBLISHED_SIF")" ]; then
	SIF_OK=yes
fi
say "config          : ${CONFIG#"$RUN"/}"
say "image (config)  : ${CFG_SIF#"$RUN"/}$([ -L "$CFG_SIF" ] && printf ' -> %s' "$(readlink "$CFG_SIF")")"
say "image (wanted)  : $PUBLISHED_SIF"
if [ "$SIF_OK" = yes ]; then
	say "status          : already on the published v2.1.0 image"
else
	say "status          : NOT the published image -- will be repointed"
fi

# ---- 3. protect what must survive -------------------------------------------
# Existing BAMs are never touched. Missing ones are NOT an error: a run stopped by
# the old image's container-startup kill typically has a mix, and STAR_paired will
# simply be rescheduled for the missing samples on the new image.
head2 "STAR alignments (never touched)"
BAM_N=0; BAM_EMPTY=0
if [ -d "$RUN/results/GETA/STAR/paired" ]; then
	while IFS= read -r bam; do
		BAM_N=$((BAM_N + 1))
		[ -s "$bam" ] || BAM_EMPTY=$((BAM_EMPTY + 1))
	done < <(find "$RUN/results/GETA/STAR/paired" -maxdepth 1 -name '*.bam')
fi

EXPECT_N=0
FASTP="$RUN/results/GETA/fastp/paired"
if [ -d "$FASTP" ]; then
	EXPECT_N=$(find "$FASTP" -maxdepth 1 -name '*_1.fastq.gz' | wc -l)
fi

if [ "$EXPECT_N" -gt 0 ]; then
	say "paired samples  : $EXPECT_N"
	say "paired BAMs     : $BAM_N present, $((EXPECT_N - BAM_N)) missing ($BAM_EMPTY zero-byte)"
	if [ "$BAM_N" -lt "$EXPECT_N" ]; then
		say "                  -> STAR_paired will re-run for the $((EXPECT_N - BAM_N)) missing sample(s)."
		say "                     That is expected: the old image killed them at container start."
	fi
else
	say "paired BAMs     : $BAM_N present ($BAM_EMPTY zero-byte)"
fi
[ "$BAM_EMPTY" -eq 0 ] || die "$BAM_EMPTY zero-byte BAM(s) -- that is the STAR sort-RAM bug, not this script's job. Fix those first."

# ---- 4. scan for the artifacts that block a correct resume -------------------
head2 "stale artifacts"
GMAP_DIR="$RUN/results/TRANSCRIPT/Gmap"
GW_TMP="$RUN/results/GETA/homolog/geneRegion_genewise.tmp"
HOOK="$RUN/results/.micromamba_hook.sh"

STALE=()
add_stale() { STALE+=("$1"); printf '  %-52s %s\n' "${1#"$RUN"/}" "$2"; }

if [ -f "$GMAP_DIR/liftoffExon.fasta" ] && [ ! -s "$GMAP_DIR/liftoffExon.fasta" ]; then
	add_stale "$GMAP_DIR/liftoffExon.fasta" "0 bytes -- gmapPrep exited 0 without bedtools; MUST be deleted"
fi
if [ -f "$GMAP_DIR/gmapExon.gff3" ] && [ ! -s "$GMAP_DIR/gmapExon.gff3" ]; then
	add_stale "$GMAP_DIR/gmapExon.gff3" "0 bytes -- gmapl rejected this genome"
fi
if [ -d "$GW_TMP" ]; then
	FAA=$(find "$GW_TMP" -maxdepth 1 -name '*.faa' | wc -l)
	GFF=$(find "$GW_TMP" -maxdepth 1 -name '*.gff' | wc -l)
	if [ "$FAA" -eq 0 ]; then
		# An empty leftover. Snakemake removes a directory() output before re-running its
		# rule, so this needs no help. Do not send the operator to rerun_genewise.sh.
		say "  geneRegion_genewise.tmp: empty -- nothing to do"
	elif [ "$GFF" -eq 0 ]; then
		add_stale "$GW_TMP" "partial checkpoint: $FAA groups, 0 GeneWise GFFs"
	elif [ "$GFF" -lt "$FAA" ]; then
		say "  geneRegion_genewise.tmp: $FAA groups / $GFF GFFs -- GeneWise stopped partway. Neither this"
		say "                           script nor bin/rerun_genewise.sh handles that; resolve it first."
	else
		say "  geneRegion_genewise.tmp: $FAA groups / $GFF GFFs -- GeneWise has run; use bin/rerun_genewise.sh instead"
	fi
fi
# The Snakemake-side hook cache is generated inside the container by the first rule to
# run and rebuilt only when absent, so it belongs to whichever image produced it. It is
# stale whenever that is not the image this run is about to use.
#
# Do NOT key this off SIF_OK. An operator who repoints the image by hand -- which is
# exactly what HANDOFF_2026-07-08 §3.2 tells them to do -- leaves SIF_OK=yes and a hook
# cache from the previous image, and this script used to call that run prepared.
HOOK_STALE=no
if [ -f "$HOOK" ]; then
	if [ ! -e "$CFG_SIF" ]; then
		HOOK_STALE=yes
		add_stale "$HOOK" "the configured image is missing; the cache cannot be trusted"
	elif [ "$SIF_OK" = no ]; then
		HOOK_STALE=yes
		add_stale "$HOOK" "built by the image this script is about to replace"
	elif [ "$HOOK" -ot "$CFG_SIF" ]; then
		HOOK_STALE=yes
		add_stale "$HOOK" "older than the image in use -- built by an earlier one"
	fi
fi
[ "${#STALE[@]}" -gt 0 ] || say "  none"

# ---- 5. report / apply -------------------------------------------------------
RESUME="SYLVAN_CONFIG=${CONFIG#"$RUN"/} ./bin/annotate.sh --rerun-triggers mtime"

if [ "$APPLY" != yes ]; then
	head2 "DRY RUN -- nothing changed"
	say "Re-run with --apply to:"
	[ "$SIF_OK" = no ] && say "  * repoint ${CFG_SIF#"$RUN"/} at the published image"
	[ "${#STALE[@]}" -gt 0 ] && say "  * move ${#STALE[@]} artifact(s) into results/_preflight_backup_<stamp>/"
	say ""
	say "Then, from $RUN:"
	say "  $RESUME -n     # dry run: Helixer must NOT appear; the missing STAR_paired jobs must"
	say "  $RESUME"
	exit 0
fi

head2 "applying"
if [ "${#STALE[@]}" -eq 0 ] && [ "$SIF_OK" = yes ]; then
	say "nothing to do -- this run is already prepared"
	say ""
	say "From $RUN:"
	say "  $RESUME -n     # dry run: Helixer must NOT appear; the missing STAR_paired jobs must"
	say "  $RESUME"
	exit 0
fi

# Check the image BEFORE moving anything: a bad image should leave the run untouched.
if [ "$SIF_OK" = no ]; then
	if [ -f "$PUBLISHED_SIF.sha256" ]; then
		say "verify  $(basename "$PUBLISHED_SIF") against its .sha256 (a 10 GB image takes about a minute)"
		if ! ( cd "$(dirname "$PUBLISHED_SIF")" && sha256sum -c "$(basename "$PUBLISHED_SIF").sha256" >/dev/null 2>&1 ); then
			die "sha256 mismatch for $PUBLISHED_SIF -- refusing to touch this run"
		fi
		say "verify  OK"
	else
		say "verify  no .sha256 beside $PUBLISHED_SIF -- skipping the integrity check"
	fi
fi

BK="$RUN/results/_preflight_backup_$(date +%Y%m%d-%H%M%S)"
mkdir -p "$BK"

for f in ${STALE[@]+"${STALE[@]}"}; do
	mv "$f" "$BK/"
	say "moved   ${f#"$RUN"/}"
done

# The flock file belongs to the cache we just moved out; the Snakefile recreates both.
if [ "$HOOK_STALE" = yes ]; then
	rm -f "$HOOK.lock"
fi

if [ "$SIF_OK" = no ]; then
	if [ -e "$CFG_SIF" ] && [ ! -L "$CFG_SIF" ]; then
		mv "$CFG_SIF" "$BK/$(basename "$CFG_SIF").old"
		say "moved   ${CFG_SIF#"$RUN"/} (old image)"
	else
		rm -f "$CFG_SIF"
	fi
	ln -sfn "$PUBLISHED_SIF" "$CFG_SIF"
	say "linked  ${CFG_SIF#"$RUN"/} -> $PUBLISHED_SIF"
fi

say ""
say "backup  : ${BK#"$RUN"/}"
say "rollback: move everything back out of it, then \`touch\` the restored files."
say ""
say "Now, from $RUN:"
say "  $RESUME -n     # dry run: Helixer must NOT appear; the missing STAR_paired jobs must"
say "  $RESUME"
